    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
        // TODO add your handling code here:
        String res, res1;
        double metros, multi, ini, resta, cuotas;
        
        if (txtMetros.getText().trim().isEmpty()) {
             JOptionPane.showMessageDialog(this, "Digite por favor el n√∫mero de metros cuadrados", "Error", JOptionPane.ERROR_MESSAGE);
             txtMetros.requestFocusInWindow();

        } else {

        metros = Double.parseDouble(txtMetros.getText());
               
        multi = metros * 80000;
        ini = multi * 0.35;
        resta = multi - ini;
        cuotas = resta / 12;

        res = String.valueOf(ini);
        res1 = String.valueOf(cuotas);
        txtCuotaIni.setText(res);
        txtCuotas.setText(res1);

        }
    }//GEN-LAST:event_cmdCalcularActionPerformed

